#!/usr/bin/env python3
"""
comp-018 scope/output validator + summary emitter.

stdlib only. Reads inputs/{targets,query-strategy}.json + outputs/compounds.json,
validates schema, prints quick provenance + tier counts, and writes
outputs/scope-summary.md.

Per the comp-013/comp-014 reproducibility precedent: anyone who clones the
repo can run `python3 scripts/scope_validate.py` from this folder and
reproduce the summary.
"""

import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
INPUTS = ROOT / "inputs"
OUTPUTS = ROOT / "outputs"


def load_json(path: Path):
    with path.open() as f:
        return json.load(f)


def validate_targets(targets_doc):
    targets = targets_doc.get("targets", [])
    required = {"id", "uniprot", "node_class", "msu_relevance", "intervention_direction"}
    errors = []
    for t in targets:
        missing = required - set(t.keys())
        if missing:
            errors.append(f"target {t.get('id', '?')}: missing fields {missing}")
    return targets, errors


def validate_compounds(compounds_doc):
    compounds = compounds_doc.get("compounds", [])
    required = {"name", "compound_class", "target_node", "platform_relevance"}
    errors = []
    for c in compounds:
        missing = required - set(c.keys())
        if missing:
            errors.append(f"compound {c.get('name', '?')}: missing fields {missing}")
    return compounds, errors


def tier_counts(compounds):
    counts = {}
    for c in compounds:
        tier = c.get("platform_relevance", {}).get("tier", "UNKNOWN")
        counts[tier] = counts.get(tier, 0) + 1
    return counts


def chembl_coverage(compounds):
    have_chembl_record = 0
    have_chembl_anticomplement = 0
    no_chembl = 0
    for c in compounds:
        cid = c.get("chembl_id")
        ch_anti = (c.get("chembl_anticomplement_data") or "").strip()
        if cid:
            have_chembl_record += 1
        else:
            no_chembl += 1
        # 'have anticomplement data' = field is non-empty and does NOT start with EMPTY/no
        if ch_anti and not ch_anti.upper().startswith("EMPTY") and not ch_anti.lower().startswith("no chembl"):
            have_chembl_anticomplement += 1
    return have_chembl_record, have_chembl_anticomplement, no_chembl


def main():
    targets_doc = load_json(INPUTS / "targets.json")
    strategy_doc = load_json(INPUTS / "query-strategy.json")
    compounds_doc = load_json(OUTPUTS / "compounds.json")

    targets, terrors = validate_targets(targets_doc)
    compounds, cerrors = validate_compounds(compounds_doc)

    if terrors or cerrors:
        print("VALIDATION ERRORS:")
        for e in terrors + cerrors:
            print(f"  - {e}")
        sys.exit(1)

    tiers = tier_counts(compounds)
    have_chembl, have_chembl_anti, no_chembl = chembl_coverage(compounds)

    summary = []
    summary.append("# comp-018 — Scope-validate Summary\n")
    summary.append(f"**Generated by:** `scripts/scope_validate.py`\n")
    summary.append(f"**Targets defined:** {len(targets)}\n")
    summary.append(f"**Compounds catalogued:** {len(compounds)}\n\n")
    summary.append("## Tier distribution\n")
    summary.append("| Tier | Count |\n|---|---|\n")
    for tier in sorted(tiers.keys()):
        summary.append(f"| {tier} | {tiers[tier]} |\n")

    summary.append("\n## ChEMBL coverage\n")
    summary.append(f"- Compounds with any ChEMBL record: {have_chembl} / {len(compounds)}\n")
    summary.append(f"- Compounds with curated ChEMBL anticomplement data: {have_chembl_anti} / {len(compounds)}\n")
    summary.append(f"- Compounds with no ChEMBL record: {no_chembl} / {len(compounds)}\n")
    summary.append(f"- ChEMBL anticomplement coverage rate: {have_chembl_anti/len(compounds)*100:.1f}%\n")

    summary.append("\n## Targets defined\n")
    summary.append("| Node | Class | MSU relevance |\n|---|---|---|\n")
    for t in targets:
        relev = t.get("msu_relevance", "")
        if " " in relev:
            relev = relev.split(" ")[0]
        summary.append(f"| {t['id']} | {t['node_class']} | {relev} |\n")

    summary.append("\n## Sources executed (Phase 1)\n")
    for q in strategy_doc.get("search_macros_executed_2026_05_08", []):
        summary.append(f"- `{q['query'][:80]}` ({q['tool']}): {q.get('result_count', '?')} results — {q.get('yield', '')}\n")

    out_path = OUTPUTS / "scope-summary.md"
    with out_path.open("w") as f:
        f.write("".join(summary))

    print(f"Wrote {out_path}")
    print(f"Compounds: {len(compounds)} | Targets: {len(targets)}")
    print(f"Tier counts: {tiers}")
    print(f"ChEMBL anticomplement coverage: {have_chembl_anti}/{len(compounds)}")


if __name__ == "__main__":
    main()
